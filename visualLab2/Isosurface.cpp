template <class T> typename Isosurface<T>::IsoVertex Isosurface<T>::Interpolate(float fX1, float fY1, float fZ1, float fX2, float fY2, float fZ2)//�ڱߣ�����Ϊ (x1,y1,z1),(x2,y2,z2)����Ѱ�ҽ���
{
    T val1 = (*volume)(fX1, fY1, fZ1);
    T val2 = (*volume)(fX2, fY2, fZ2);//�ڶ��� 1��2�ϵı���ֵ

    //��������ֵ��Ľ��㣬�ڹ�һ�����λ��t��λ��[0,1]��)
    float t = float((isoLevel - val1)) / (val2 - val1);

    IsoVertex point;//ͨ���ڶ���1��2�в�ֵ�����㽻�㣬ʹ�����Բ�ֵ��ʹ�������tֵ
    point.x = m_fCellLengthX * (fX1 + t * (fX2 - fX1));//����x����
    point.y = m_fCellLengthY * (fY1 + t * (fY2 - fY1));//����y����
    point.z = m_fCellLengthZ * (fZ1 + t * (fZ2 - fZ1));//����z����Ĺ�ʽ
    return point;//���رߺ͵�ֵ��Ľ���
}

template <class T> UnstructuredGrid3D* Isosurface<T>::CreateIsoMesh()//��������
{
    typename IsoVertices::iterator  mapIterator = isoVertices.begin();
    typename IsoTriangles::iterator vecIterator = isoTriangles.begin();

    for(unsigned int nextID=0;mapIterator!=isoVertices.end();++nextID,++mapIterator)
        (*mapIterator).second.newID = nextID;				//Compact points

    for(;vecIterator != isoTriangles.end();++vecIterator)	//Compact triangles
        for (unsigned int i = 0; i < 3; ++i)
        {
            unsigned int newID = isoVertices[(*vecIterator).pointID[i]].newID;
            (*vecIterator).pointID[i] = newID;
        }

    int nVertices  = isoVertices.size();					//�������еĶ���������ε�UnstructuredGrid3D ����
    int nTriangles = isoTriangles.size();
    UnstructuredGrid3D* grid = new UnstructuredGrid3D(nVertices,nTriangles);

    float vertex[3];										//��������
    mapIterator = isoVertices.begin();
    for (unsigned int i = 0; i < nVertices; i++, mapIterator++)
    {
        vertex[0] = (*mapIterator).second.x;
        vertex[1] = (*mapIterator).second.y;
        vertex[2] = (*mapIterator).second.z;
        grid->setPoint(i,vertex);
    }

    int tri[3];												//����������
    vecIterator = isoTriangles.begin();
    for (unsigned int i = 0; i < nTriangles; i++, vecIterator++)
    {
        tri[0]   = (*vecIterator).pointID[0];
        tri[1]   = (*vecIterator).pointID[1];
        tri[2]   = (*vecIterator).pointID[2];
        grid->setCell(i,tri);
    }

    Clear();												//�ͷ����оֲ����ݽṹ

    return grid;											//���ص�ֵ������
}


